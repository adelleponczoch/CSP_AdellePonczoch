# Adelle Ponczoch, Silly Sentences Python

name = input ("Give me a name: ").strip()

animal = input ("Give me an animal type: ").strip().lower()

verb = input ("Give me a present tense verb: ").strip().lower()

dessert = input ("Give me a dessert: ").strip().lower()

time = input ("Give me a time of day: ").strip().lower()

print(f"Every day, {name} the {animal} would {verb} for hours, or at least until their daily {dessert} gorge at {time}.")